#진수(base) 변환
#*****************************************************************************************************#
number1 = 154#(10진수)
number2 = 0b10011010#(2진수)
number3 = 0x9A#(16진수) => 2진수를 뒤에서부터 4자리씩 끊음
number4 = 0o232#(8진수) => 2진수를 뒤에서부터 3자리씩 끊음

print(number1)
print(number2)
print(number3)
print(number4)

print(bin(number1))#10진수 => 2진수
print(hex(number1))#10진수 => 16진수
print(oct(number1))#10진수 => 8진수


#*****************************************************************************************************#
#아스키코드와 진수(base) 변환
#*****************************************************************************************************#
print(chr(""))              #정수 => 문자(아스키코드)
print(ord(""))              #문자 => 정수
print(hex(ord("")))         #문자 => 정수(10진수) => 16진수

